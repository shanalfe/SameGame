/**
* La classe <code>Carapuce</code> (couleur bleu) représente un bloc.
*
* @version 1.0
* @author Shana LEFEVRE & Arthur DECORBEZ
* @see Bloc
*/
public class Carapuce extends Bloc
{
	/**
	* Le constructeur récupère les variables de Bloc et y associe sa couleur à B.
	*/
	public Carapuce()
	{
		super();
		super.color = 'B';
	}
}