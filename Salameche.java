/**
 * La classe <code>Salameche</code> (couleur rouge) représente un bloc.
 * @version 1.0
 * @author Arthur DECORBEZ & Shana LEFEVRE
 * @see Bloc.
 */
public class Salameche extends Bloc {
    
	/**
	* Le constructeur récupère les variables de Bloc et y associe sa couleur à R.
	*/
    public Salameche (){
       
        super ();
        this.color = 'R';
    }
    
}
