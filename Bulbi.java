/**
 * La classe <code>Bulbi</code> (couleur vert) représente un bloc.
 * @version 1.0
 * @author Arthur DECORBEZ & Shana LEFEVRE
 * @see Bloc.
 */
public class Bulbi extends Bloc {
    
    /**
	* Le constructeur récupère les variables de Bloc et y associe sa couleur à V.
	*/
    public Bulbi (){
       
        super();
        super.color = 'V';
    }
}
